import sys

input = sys.stdin.readline
N = int(input().rstrip())
myMap = [[' ' for _ in range(N)] for _ in range(N)]


def solve(n:int, x, y):
    if(n == 1):
        myMap[x][y] = "*"
        return
    
    div = n // 3
    for i in range(3):
        for j in range(3):
            if(i == 1 and j == 1):
                continue
        
            solve(div, x + (div * i), y + (div * j))
    
    return

solve(N, 0, 0)

for i in range(N):
    for j in range(N):
        print(myMap[i][j],end='')
    print()